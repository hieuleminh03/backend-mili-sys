        api: __DIR__.'/../routes/api.php',
        health: '/up',
    )
    ->withMiddleware(function (Middleware $middleware) {
        // Middleware is now managed in the routes file, not globally
    })
    ->withExceptions(function (Exceptions $exceptions) {
        // The Handler class will be discovered automatically
    })->create();
